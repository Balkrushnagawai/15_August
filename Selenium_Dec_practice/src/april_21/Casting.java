package april_21;

public class Casting {
	public static void main(String[] args) {
		//Premative Casting
			//Implicite casting(Widning casting)
				int a= 5;
				double b=a;
				System.out.println(b);
				
			//Explicite casting
				double c=10.39;
				int d=(int)c;
				System.out.println(d);
			//Boolean casting
		//Non premative casting
			//Up casting
			//Down Casting	
	}

}
