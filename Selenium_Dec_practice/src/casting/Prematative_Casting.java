package casting;

public class Prematative_Casting {
	public static void main(String[] args) {
		//1. Implicite Casting /Widenind Casting
		int a=1267767; //4 byte
		long b=a; //8 byte
		
		System.out.println(a);
		System.out.println(b);
		
		//2. Explicite casting
		int c=1267767;
		short d=(short) c;
		System.out.println(c);
		System.out.println(d);
		
		
		//3. Boolean casting
	}
	

}
